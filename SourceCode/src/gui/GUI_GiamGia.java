	@Override
	public void actionPerformed(ActionEvent e) {
		Object o = e.getSource();
		if(o.equals(btnHuy)) {
			dispose();
		}else if(o.equals(btnXacNhan)) {
			dao_SanPham.suaGiamGia(GUI_Sach.getTable().getValueAt(GUI_Sach.getTable().getSelectedRow(), 0).toString(), 
					txtPhanTramGiam.getText(), dft.format(jNgayBD.getDate()), dft.format(jNgayKT.getDate()));
			JOptionPane.showMessageDialog(null, "Thêm thành công");
			dispose();
		}else if(o.equals(btnLamMoi)) {
			jNgayBD.setDate(null);
			jNgayKT.setDate(null);
			txtPhanTramGiam.setText("");
		}
		
	}
