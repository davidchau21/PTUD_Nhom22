	@Override
	public void actionPerformed(ActionEvent e) {
		Object o  = e.getSource();
		if (o.equals(btnXacNhan)) {
			String sdt = txtSDT.getText();
			if(sdt.equals("")) {
				JOptionPane.showMessageDialog(this, "Bạn chưa nhập số điện thoại!!");
			}else {
					String ma = dao_nhanVien.getMaNVTheoSDT(sdt);
					TaiKhoan tk = dao_taiKhoan.getTaiKhoanTheoMaNV(ma);
					if(tk == null) {
						JOptionPane.showMessageDialog(this, "Số điện thoại không có tài khoản"); 
						txtSDT.setText("");
					}
					String ma1 = "";
					String txtMk1 = txtMatKhau1.getText();
					String txtMk2 = txtMatKhau2.getText();
					if (tk != null) {
						ma1 = tk.getNhanVien().getMaNhanVien();
						if(ma1.contains(ma)) {
							jLabel1.setText("Đổi Mật Khẩu");
							jPanel2.setBorder(javax.swing.BorderFactory.createTitledBorder(javax.swing.BorderFactory.createEtchedBorder(), "Nhập mật khẩu mới"));
							lbPass1.setVisible(true);
							txtMatKhau1.setVisible(true);
							lbPass2.setVisible(true);
							txtMatKhau2.setVisible(true);
							lbSdt.setVisible(false);
							txtSDT.setVisible(false);
							chkHienThiMK.setVisible(true);
//							 String maTK = tk.getMaTaiKhoan();
//							 lbSdt.setText("Tên tài khoản của bạn");
//							 txtSDT.setText(maTK);
//							 txtSDT.setEditable(false);
							if (!txtMk1.equals("") && !txtMk2.equals("")) {
					
								if(txtMk1.equals(txtMk2)) {
									if(dao_taiKhoan.resetPass(txtMk1, sdt)) {
										JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công - Mời bạn đăng nhập ");
									}
								}else {
									JOptionPane.showMessageDialog(this, "Mật khẩu không khớp nhau");
									txtMatKhau1.setText("");
									txtMatKhau2.setText("");
								}
								
							} 
//								else {
//								JOptionPane.showMessageDialog(this, "Chưa nhập mật khẩu");
//							}
						}
					}
				
			}
				
		}else if (o.equals(chkHienThiMK)) {
			if (chkHienThiMK.isSelected()) {
                txtMatKhau1.setEchoChar((char) 0);
                txtMatKhau2.setEchoChar((char) 0);
            } else {
                txtMatKhau1.setEchoChar('*');
                txtMatKhau2.setEchoChar('*');
            }
		}else if (o.equals(btnDangNhap)) {
			GUI_DangNhap gui_DangNhap =  new GUI_DangNhap();
			gui_DangNhap.setLocationRelativeTo(null);
			gui_DangNhap.setDefaultCloseOperation(EXIT_ON_CLOSE);
			gui_DangNhap.setVisible(true);
			dispose();
		}
	}
		
