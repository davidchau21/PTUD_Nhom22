	@Override
	public void actionPerformed(ActionEvent e) {
		Object o = e.getSource();
		if(o.equals(btnThem)) {
                    GUI_ThemSach gui_ThemSach = new GUI_ThemSach();
                    gui_ThemSach.setVisible(true);
                    gui_ThemSach.setLocationRelativeTo(null);
                    gui_ThemSach.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                    edit = 1;
		}else if(o.equals(btnXoa)) {
			if(table.getSelectedRow()!=-1) {
				if(JOptionPane.showConfirmDialog(this, "Có chắc chắn xóa không","Cảnh báo",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
					dao_SanPham.xoaSach(model.getValueAt(table.getSelectedRow(), 0).toString());
					model.removeRow(table.getSelectedRow());
					JOptionPane.showMessageDialog(this, "Xóa thành công");
				}
			}else {
				JOptionPane.showMessageDialog(this, "Bạn chưa chọn sách cần xóa");
			}
		}else if(o.equals(btnSua)) {
			int row = table.getSelectedRow();
			if(row!=-1) {
				GUI_ThemSach gui_ThemSach = new GUI_ThemSach();
				gui_ThemSach.setVisible(true);
				gui_ThemSach.setLocationRelativeTo(null);
				gui_ThemSach.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
				gui_ThemSach.getLbMa().setText(model.getValueAt(row, 0).toString());
				gui_ThemSach.getTxtTenSach().setText(model.getValueAt(row, 1).toString());
				gui_ThemSach.getTxtSoLuong().setText(model.getValueAt(row, 2).toString());
				gui_ThemSach.getTxtGiaNhap().setText(model.getValueAt(row, 3).toString());
				gui_ThemSach.getTxtNamXB().setText(model.getValueAt(row, 4).toString());
				gui_ThemSach.getTxtSoTrang().setText(model.getValueAt(row, 5).toString());
				gui_ThemSach.getCbbNhaXB().setSelectedItem(model.getValueAt(row, 6).toString());
				gui_ThemSach.getCbbTacGia().setSelectedItem(model.getValueAt(row, 7).toString());
				gui_ThemSach.getCbbNCC().setSelectedItem(model.getValueAt(row, 8).toString());
				gui_ThemSach.getTxtMoTa().setText(model.getValueAt(row, 9).toString());
				gui_ThemSach.getCbbLoaiSach().setSelectedItem(model.getValueAt(row, 10).toString());
				GUI_ThemSach.getLbAnh().setIcon(GUI_ThemSach.ResizeImage(String.valueOf(dao_SanPham.gethinhAnhTheoMa(model.getValueAt(row, 0).toString()))));
				edit=2;
			}else {
				JOptionPane.showMessageDialog(this, "Bạn chưa chọn sách cần sửa");
			}
			
		}else if(o.equals(btnTim)) {
			String tenSP = "%"+txtTenSach.getText().trim()+"%";
			String namXB = "%"+txtNamXB.getText().trim()+"%";
			String l = dao_LoaiSanPham.getMaLoaiTheoChiTiet(cbbLoaiSach.getSelectedItem().toString());
			String nhaXB = dao_NhaXuatBan.getMaNXBTheoTen(cbbNXB.getSelectedItem().toString());
			String t = dao_TacGia.getMaTheoTen(cbbTacGia.getSelectedItem().toString());
			String n = dao_NhaCungCap.getMaNCCTheoTen(cbbNCC.getSelectedItem().toString());
			
			if(cbbLoaiSach.getSelectedIndex()==0) {
				l = "";
			}
			if(cbbNCC.getSelectedIndex()==0) {
				n = "";
			}
			if(cbbNXB.getSelectedIndex()==0) {
				nhaXB = "";
			}
			if(cbbTacGia.getSelectedIndex()==0) {
				t = "";
			}
			if(chkTenSach.isSelected())
				tenSP = txtTenSach.getText().trim();
			if(chkNamXB.isSelected())
				namXB = txtNamXB.getText().trim();
			if(txtTenSach.getText().trim().equals(""))
				tenSP = "%%";
			if(txtNamXB.getText().trim().equals(""))
				namXB = "%%";
			model.setRowCount(0);
			for (SanPham sach : dao_SanPham.getSachTheoDK(tenSP, namXB, l, nhaXB, t, n)) {
				String nxb = dao_NhaXuatBan.getTenNXBTheoMa(sach.getNhaXB().getMaNXB());
				String tg = dao_TacGia.getTenTGTheoMa(sach.getTacGia().getMaTG());
				String ncc = dao_NhaCungCap.getTenNCCTheoMa(sach.getNhaCungCap().getMaNCC());
				String loai = dao_LoaiSanPham.getChiTietTheoMaLoai(sach.getLoaiSP().getMaLoai());
				Object row[] = {sach.getMaSanPham(),sach.getTenSanPham(),sach.getSoLuong(),sach.getGiaNhap(),
						sach.getNamXB(),sach.getSoTrang(),nxb,tg,ncc,sach.getMoTa(),loai};
				model.addRow(row);
			}
		}else if(o.equals(btnLamMoi)) {
			txtNamXB.setText("");
			txtTenSach.setText("");
			chkNamXB.setSelected(false);
			chkTenSach.setSelected(false);
			cbbLoaiSach.setSelectedIndex(0);
			cbbNCC.setSelectedIndex(0);
			cbbNXB.setSelectedIndex(0);
			cbbTacGia.setSelectedIndex(0);
			updateTable();
		}
	}
	
