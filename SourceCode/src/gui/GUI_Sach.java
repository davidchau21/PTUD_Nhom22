    private void loadCbo() {
		ArrayList<LoaiSanPham> dataList = dao_LoaiSanPham.getLoaiSach();
		for(LoaiSanPham loaisp : dataList) {
			cbbLoaiSach.addItem(loaisp.getChiTiet());
		}
		ArrayList<NhaCungCap> listNCC = dao_NhaCungCap.getAllNCC();
		for (NhaCungCap nhaCungCap : listNCC) {
			cbbNCC.addItem(nhaCungCap.getTenNCC());
		}
		ArrayList<TacGia> listTG = dao_TacGia.getAllTG();
		for (TacGia tacGia : listTG) {
			cbbTacGia.addItem(tacGia.getHoTen());
		}
		ArrayList<NhaXuatBan> listNXB = dao_NhaXuatBan.getAllNXB();
		for (NhaXuatBan nhaXuatBan : listNXB) {
			cbbNXB.addItem(nhaXuatBan.getTenNXB());
		}
		
		
	}
	
	
	public static void updateTable() {
		model.setRowCount(0);
		for (SanPham sach : dao_SanPham.getListSach()) {
			String nxb = dao_NhaXuatBan.getTenNXBTheoMa(sach.getNhaXB().getMaNXB());
			String tg = dao_TacGia.getTenTGTheoMa(sach.getTacGia().getMaTG());
			String ncc = dao_NhaCungCap.getTenNCCTheoMa(sach.getNhaCungCap().getMaNCC());
			String loai = dao_LoaiSanPham.getChiTietTheoMaLoai(sach.getLoaiSP().getMaLoai());
			Object row[] = {sach.getMaSanPham(),sach.getTenSanPham(),sach.getSoLuong(),sach.getGiaNhap(),
					sach.getNamXB(),sach.getSoTrang(),nxb,tg,ncc,sach.getMoTa(),loai};
			model.addRow(row);
		}
	}

