    public static void updataTable() {
    	modelVPP.setRowCount(0);
		for (SanPham sp : dao_SanPham.getAllVPP()) {
			String loai = dao_LoaiSanPham.getChiTietTheoMaLoai(sp.getLoaiSP().getMaLoai());
			String xx = dao_XuatXu.getXuatXuTheoMaXX(sp.getXuatXu().getMaXX());
			Object row [] = {sp.getMaSanPham(),sp.getTenSanPham(),sp.getSoLuong(),sp.getGiaNhap(),sp.getThuongHieu(),loai,xx};
			modelVPP.addRow(row);
		}
	}
