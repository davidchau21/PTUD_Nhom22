	@Override
	public void actionPerformed(ActionEvent e) {
		Object o = e.getSource();
		if(o.equals(btnThem)) {
                    GUI_ThemVanPhongPham gui_ThemVanPhongPham = new GUI_ThemVanPhongPham();
                    gui_ThemVanPhongPham.setVisible(true);
                    gui_ThemVanPhongPham.setLocationRelativeTo(null);
                    gui_ThemVanPhongPham.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
                    edit = 1;
		}else if(o.equals(btnXoa)) {
			if(tableVPP.getSelectedRow()!=-1) {
				if(JOptionPane.showConfirmDialog(this, "Có chắc chắn xóa không","Cảnh báo",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION) {
					dao_SanPham.xoaVPP(modelVPP.getValueAt(tableVPP.getSelectedRow(), 0).toString());
					modelVPP.removeRow(tableVPP.getSelectedRow());
					JOptionPane.showMessageDialog(this, "Xóa thành công");
				}
			}else {
				JOptionPane.showMessageDialog(this, "Bạn chưa chọn khách hàng cần xóa");
			}
		}else if(o.equals(btnSua)) {
			int row = tableVPP.getSelectedRow();
			if(row!=-1) {
				GUI_ThemVanPhongPham gui_ThemVanPhongPham = new GUI_ThemVanPhongPham();
				gui_ThemVanPhongPham.setVisible(true);
				gui_ThemVanPhongPham.setLocationRelativeTo(null);
				gui_ThemVanPhongPham.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
				gui_ThemVanPhongPham.getLbMaSP().setText(modelVPP.getValueAt(row, 0).toString());
				gui_ThemVanPhongPham.getTxtTenSP().setText(modelVPP.getValueAt(row, 1).toString());
				gui_ThemVanPhongPham.getTxtSoLuong().setText(modelVPP.getValueAt(row, 2).toString());
				gui_ThemVanPhongPham.getTxtGiaNhap().setText(modelVPP.getValueAt(row, 3).toString());
				gui_ThemVanPhongPham.getTxtThuongHieu().setText(modelVPP.getValueAt(row, 4).toString());
				gui_ThemVanPhongPham.getCbbLoaiSP().setSelectedItem(modelVPP.getValueAt(row, 5).toString());
				gui_ThemVanPhongPham.getCbbXuatXu().setSelectedItem(modelVPP.getValueAt(row, 6).toString());
				gui_ThemVanPhongPham.getLbAnh().setIcon(GUI_ThemVanPhongPham.ResizeImage(String.valueOf(dao_SanPham.gethinhAnhTheoMa(modelVPP.getValueAt(row, 0).toString()))));
				edit=2;
			}else {
				JOptionPane.showMessageDialog(this, "Bạn chưa chọn khách hàng cần sửa");
			}
			
		}else if(o.equals(btnTim)) {
			String tenSP = "%"+txtTimTenSP.getText().trim()+"%";
			String th = "%"+txtTimThuongHieu.getText().trim()+"%";
			String x = dao_XuatXu.getMaXXTheoXX(cbbXuatXu.getSelectedItem().toString()); 
			String l = dao_LoaiSanPham.getMaLoaiTheoChiTiet(cbbTenLoai.getSelectedItem().toString()); 
			if(cbbXuatXu.getSelectedIndex()==0) {
				x = "";
			}
			if(cbbTenLoai.getSelectedIndex()==0) {
				l = "";
			}
			if(chkTimHoTen.isSelected())
				tenSP = txtTimTenSP.getText().trim();
			if(chkTimSDT.isSelected())
				th = txtTimThuongHieu.getText().trim();
			if(txtTimTenSP.getText().trim().equals(""))
				tenSP = "%%";
			if(txtTimThuongHieu.getText().trim().equals(""))
				th = "%%";
			modelVPP.setRowCount(0);
			for (SanPham sp : dao_SanPham.getVPPTheoDK(tenSP,th,x,l)) {
				String loai = dao_LoaiSanPham.getChiTietTheoMaLoai(sp.getLoaiSP().getMaLoai());
				String xx = dao_XuatXu.getXuatXuTheoMaXX(sp.getXuatXu().getMaXX());
				Object row [] = {sp.getMaSanPham(),sp.getTenSanPham(),sp.getSoLuong(),sp.getGiaNhap(),sp.getThuongHieu(),loai,xx};
				modelVPP.addRow(row);
			}
		}else if(o.equals(btnLamMoi)) {
			txtTimTenSP.setText("");
			txtTimThuongHieu.setText("");
			chkTimHoTen.setSelected(false);
			chkTimSDT.setSelected(false);
			cbbTenLoai.setSelectedIndex(0);
			cbbXuatXu.setSelectedIndex(0);
			updataTable();
		}
	}
